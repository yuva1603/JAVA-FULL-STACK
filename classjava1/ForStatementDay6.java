package classjava1;

public class ForStatementDay6 {

	public static void main(String[] args) {
		
		//for(initialization;condition;increment/decrement)
		//{code executes here}
		int i;
		for(i=0;i<10;i++)
		{
			System.out.println("The Result is "+" "+i);
		}

		
		for(i=10;i>0;i--)
		{
			System.out.println("The Result is "+" "+i);
		}

	}

}
